export const getDocLabel = (type: DocumentType, country: string | null, lang: Language) => {
  if (type === DocumentType.LICENSE) return translations[lang].doc_license;
  if (type === DocumentType.NATIONAL_ID) {
    if (country === 'VE') return translations[lang].doc_cedula;
    return translations[lang].doc_id;
  }
  return '';
};
